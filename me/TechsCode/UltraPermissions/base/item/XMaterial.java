    private static final HashMap<String, XMaterial> cachedSearch;
    private static boolean isServerAncient;
    private final String ancientMaterialName;
    private final byte data;
    
    private XMaterial(final String ancientMaterialName, final byte data) {
        this.ancientMaterialName = ancientMaterialName;
        this.data = data;
    }
    
    private XMaterial(final String ancientMaterialName) {
        this.ancientMaterialName = ancientMaterialName;
        this.data = 0;
    }
    
    private XMaterial() {
        this.ancientMaterialName = null;
        this.data = 0;
    }
    
    public static Optional<XMaterial> from(final String s) {
        try {
            return Optional.of(valueOf(s));
        }
        catch (IllegalArgumentException ex) {
            return Optional.empty();
        }
    }
    
    public static XMaterial requestXMaterial(final String s, final byte i) {
        if (XMaterial.cachedSearch.containsKey(s.toUpperCase() + "," + i)) {
            return XMaterial.cachedSearch.get(s.toUpperCase() + "," + i);
        }
        for (final XMaterial value : values()) {
            if (i == 0 || XMaterial.isServerAncient) {
                if (s.toUpperCase().equals(value.ancientMaterialName) && value.data == i) {
                    XMaterial.cachedSearch.put(value.ancientMaterialName + "," + i, value);
                    return value;
                }
            }
        }
        return null;
    }
    
    public static XMaterial fromBlock(final Block block) {
        if (XMaterial.isServerAncient) {
            return requestXMaterial(block.getType().name(), block.getData());
        }
        return Arrays.stream(values()).filter(xMaterial -> xMaterial.name().equals(block.getType().name())).findFirst().orElse(null);
    }
    
    public static XMaterial fromItem(final ItemStack itemStack) {
        if (XMaterial.isServerAncient) {
            return requestXMaterial(itemStack.getType().name(), itemStack.getData().getData());
        }
        return Arrays.stream(values()).filter(xMaterial -> xMaterial.name().equals(itemStack.getType().name())).findFirst().orElse(null);
    }
    
    public static XMaterial fromMaterial(final Material material) {
        final XMaterial[] array;
        final int length;
        int i = 0;
        XMaterial xMaterial;
        return from(material.toString()).orElseGet(() -> {
            values();
            length = array.length;
            while (i < length) {
                xMaterial = array[i];
                if (xMaterial.ancientMaterialName != null && xMaterial.ancientMaterialName.equals(material.toString())) {
                    return xMaterial;
                }
                else {
                    ++i;
                }
            }
            return null;
        });
    }
    
    public void setMaterialOf(final ItemStack itemStack) {
        this.getBukkitMaterialData().ifPresent(itemStack::setData);
    }
    
    public void setMaterialOf(final Block block) {
        final Optional<MaterialData> bukkitMaterialData = this.getBukkitMaterialData();
        if (bukkitMaterialData.isPresent()) {
            block.setType(bukkitMaterialData.get().getItemType());
            if (XMaterial.isServerAncient) {
                block.setData(bukkitMaterialData.get().getData());
            }
        }
    }
    
    public Optional<ItemStack> getAsItemStack() {
        final Optional<Material> bukkitMaterial = this.getBukkitMaterial();
        if (!bukkitMaterial.isPresent()) {
            return Optional.empty();
        }
        if (XMaterial.isServerAncient) {
            return Optional.of(new ItemStack((Material)bukkitMaterial.get(), 1, (short)this.data));
        }
        return Optional.of(new ItemStack((Material)bukkitMaterial.get()));
    }
    
    @Deprecated
    public Material parseMaterial() {
        return this.getBukkitMaterial().orElse(null);
    }
    
    @Deprecated
    public ItemStack parseItem() {
        return this.getAsItemStack().orElse(null);
    }
    
    public Optional<Material> getBukkitMaterial() {
        final Material matchMaterial = Material.matchMaterial(this.name());
        if (matchMaterial != null) {
            return Optional.of(matchMaterial);
        }
        if (this.ancientMaterialName != null) {
            return Optional.ofNullable(Material.matchMaterial(this.ancientMaterialName));
        }
        return Optional.empty();
    }
    
    public Optional<MaterialData> getBukkitMaterialData() {
        final Optional<Material> bukkitMaterial = this.getBukkitMaterial();
        if (!bukkitMaterial.isPresent()) {
            return Optional.empty();
        }
        if (XMaterial.isServerAncient) {
            return Optional.of(new MaterialData((Material)bukkitMaterial.get(), this.data));
        }
        return Optional.of(new MaterialData((Material)bukkitMaterial.get()));
    }
    
    public boolean isItemNewlyAdded() {
        return this.ancientMaterialName == null;
    }
    
    public String getHumanName() {
        return Tools.getEnumName(this);
    }
    
    public XMaterial orElse(final XMaterial xMaterial) {
        return this.getBukkitMaterial().isPresent() ? this : xMaterial;
    }
    
    static {
        cachedSearch = new HashMap<String, XMaterial>();
        XMaterial.isServerAncient = (Material.matchMaterial("RED_WOOL") == null);
    }
